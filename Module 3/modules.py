#Modules are containers for Python code
#Modules contain functions, classes, and variables that are all related
#and used for specific purposes
#Modules help keeps code organized and containable
#you call modules using the import statement

import math

my_num = 25.0

#This calls the sqrt function from the math module
print(f" The square root of {my_num} is {math.sqrt(my_num)}")

#Modules are different from classes as they do not use instantiation
#They are also different from libraries as modules only use a single python file
#Libraries are collections of modules